#!/usr/bin/python
# ============================================================================
# aaf.py — 抗混叠滤波器（Anti-Aliasing Filter）
# ============================================================================
# 用途：
#   在 Bayer RAW 域对同色通道做低通滤波，抑制高频分量，
#   防止后续去马赛克（CFA 插值）时产生伪色、拉链效应等混叠伪影。
#
# 原理：
#   Bayer 传感器中同色像素间隔为 2（隔行隔列），因此高频细节在
#   每个同色通道上表现为混叠。AAF 在 Demosaic 之前对 RAW 数据
#   做温和的低通滤波，削弱对角线方向的高频分量。
#
# 核心公式：
#   使用 5×5 卷积核，kernel 中仅同色像素位置非零（间隔为 2），
#   异色像素位置为 0，因此滤波不会混合 R/G/B 通道。
#   归一化因子为 1/16，中心权重 8/16 = 0.5（保真），
#   四周 8 个对角邻居各贡献 1/16 ≈ 6.25%。
# ============================================================================

import numpy as np
from scipy.ndimage import correlate  # scipy 的互相关函数，等价于不翻转 kernel 的卷积


class AAF:
    """
    抗混叠滤波器 (Anti-Aliasing Filter)

    在 Bayer RAW 域对同色通道做低通滤波，抑制高频分量，
    防止后续去马赛克 (CFA 插值) 时产生伪色、拉链效应等混叠伪影。

    kernel 设计中所有 0 位置恰好对应 Bayer 阵列中的异色像素，
    因此这个滤波仅在「同色通道」内部做加权平均，不会混合 R/G/B。
    """

    def __init__(self, img):
        """
        初始化 AAF 滤波器。

        参数：
            img: 已做完 BLC 的 RAW 图像（numpy 数组，Bayer 格式，通常为 uint16）
        """
        self.img = img

    def padding(self):
        """
        边界填充：用镜像反射 (reflect) 模式向外扩展 2 个像素，
        保证 5×5 卷积核在图像边缘也能正常计算。

        返回：
            填充后的图像数组
        """
        img_pad = np.pad(self.img, (2, 2), 'reflect')
        return img_pad

    def execute(self):
        """
        执行抗混叠滤波。

        5×5 kernel 的结构与含义（已归一化，除以 16）：

            [1  0  1  0  1]       — 对角同色像素，权重各 1/16
            [0  0  0  0  0]       — 0 = 异色像素，不参与计算
            [1  0  8  0  1]       — 中心权重 8/16 = 0.5（保真）
            [0  0  0  0  0]       — 0 = 异色像素，不参与计算
            [1  0  1  0  1]       — 对角同色像素，权重各 1/16

        Bayer 阵列中同色像素间隔为 2（隔行隔列），所以：
        - kernel 中取值的 9 个位置均为同一颜色通道（R/G/B 之一）
        - 四周 8 个邻居各贡献 6.25%，中心像素贡献 50%
        - 这是一个温和的低通滤波：主要削弱对角线方向高频，
          水平/垂直方向的高频保留较多（kernel 在行列方向未覆盖相邻同色像素）

        返回：
            滤波后的 RAW 图像
        """
        # 先做边界填充，保证边缘像素也能正常卷积
        img_pad = self.padding()
        raw_h = self.img.shape[0]  # 图像高度
        raw_w = self.img.shape[1]  # 图像宽度

        # scipy.ndimage.correlate: 将 kernel 与图像做互相关
        # 由于 kernel 关于中心对称，这里互相关与卷积结果相同
        # kernel 中 0 的位置恰好是 Bayer 中异色像素的位置，保证不混色
        aaf_img = correlate(self.img, np.array([[1, 0, 1, 0, 1],
                                                [0, 0, 0, 0, 0],
                                                [1, 0, 8, 0, 1],
                                                [0, 0, 0, 0, 0],
                                                [1, 0, 1, 0, 1]]) / 16)
        self.img = aaf_img
        return self.img
